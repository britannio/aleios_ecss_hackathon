class Building {
  late final LatLng _location2;
  final String name;
  final BuildingCapacityStatus capacityStatus;
  final int percentFull;
  final String codeSpreadSheet;

  Building({
    required this.capacityStatus,
    required this.percentFull,
    required LatLng location,
    required this.name,
    this.codeSpreadSheet = '1',
  }) : _location2 = location;

  LatLng get location {
    return polygon.isEmpty ? _location2 : polygon.first;
  }

  String get shortName => name.substring(0, 4);

  List<LatLng> get polygon {
    return buildingData[codeSpreadSheet] ?? buildingData['1']!;
  }

  Widget get icon {
    late final IconData data;
    if (percentFull < 20) {
      data = FontAwesomeIcons.circle;
    } else if (percentFull < 80) {
      data = FontAwesomeIcons.circleHalfStroke;
    } else {
      data = FontAwesomeIcons.solidCircle;
    }

    return FaIcon(data, color: capacityStatus.color);
  }
}

enum BuildingCapacityStatus { full, available, closed }

extension BuildingCapacityStatusX on BuildingCapacityStatus {
  Color get color {
    switch (this) {
      case BuildingCapacityStatus.full:
        return Colors.red;
      case BuildingCapacityStatus.available:
        return Colors.green;
      case BuildingCapacityStatus.closed:
        return Colors.black;
    }
  }
}

